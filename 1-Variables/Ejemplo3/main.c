#include <stdio.h>

void main(void)
{

    int numero = 0;
    printf("Ingrese un numero\n");
    scanf("%d",&numero);
    printf("Numero ingresado: %d\n",numero);
    printf("Numero ingresado en hexa: %x\n",numero);
    printf("Numero ingresado en octal: %o\n",numero);
    
}
